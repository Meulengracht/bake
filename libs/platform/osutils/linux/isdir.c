#include <chef/platform.h>
#include <errno.h>
#include <sys/stat.h>

int platform_isdir(const char* path)
{
	struct stat st;
	if (stat(path, &st) != 0) {
		return -1;
	}
	return S_ISDIR(st.st_mode) != 1;
}
